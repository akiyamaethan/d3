// ------------------- Persistence (Flyweight + Memento) -------------------
// Only store modified cells here. Unmodified cells are generated by luck() on demand.
type CellMemento = { tokenValue: number | null }; // null = empty
const modifiedCells = new Map<string, CellMemento>(); // store modified cells (memento)

// Utility keys & conversions
function cellKey(i: number, j: number) {
  return `${i},${j}`;
}
function cellCenter(i: number, j: number): [number, number] {
  return [(i + 0.5) * TILE_DEGREES, (j + 0.5) * TILE_DEGREES];
}
function cellToBounds(i: number, j: number) {
  return leaflet.latLngBounds([i * TILE_DEGREES, j * TILE_DEGREES], [
    (i + 1) * TILE_DEGREES,
    (j + 1) * TILE_DEGREES,
  ]);
}
function latLngToCell(lat: number, lng: number) {
  return {
    i: Math.floor(lat / TILE_DEGREES),
    j: Math.floor(lng / TILE_DEGREES),
  };
}

// ------------------- Visual caches (transient) -------------------
const cellLayers = new Map<string, leaflet.Rectangle>();
const valueMarkers = new Map<string, leaflet.Marker>();

// ------------------- Cell state API -------------------
function getCellTokenValue(i: number, j: number): number | null {
  const key = cellKey(i, j);
  if (modifiedCells.has(key)) return modifiedCells.get(key)!.tokenValue;
  const spawnChance = luck(`${i},${j},spawn`);
  if (spawnChance < CACHE_SPAWN_PROBABILITY) {
    return 1 << Math.floor(luck(`${i},${j},value`) * 4);
  }
  return null;
}

function setCellTokenValue(i: number, j: number, tokenValue: number | null) {
  const key = cellKey(i, j);
  modifiedCells.set(key, { tokenValue });
  saveGame(); // persist memento changes
}

// ------------------- Save / Load -------------------
function saveGame() {
  try {
    const obj = {
      modifiedCells: Array.from(modifiedCells.entries()), // [ [key, {tokenValue}], ... ]
      playerLat,
      playerLng,
      heldToken, // can be null
      hasWon,
    };
    localStorage.setItem(STORAGE_KEY, JSON.stringify(obj));
  } catch (err) {
    console.error("Failed to save game:", err);
  }
}

function loadGame(): boolean {
  try {
    const raw = localStorage.getItem(STORAGE_KEY);
    if (!raw) return false;
    const parsed = JSON.parse(raw);
    if (!parsed) return false;

    // restore modifiedCells
    modifiedCells.clear();
    if (Array.isArray(parsed.modifiedCells)) {
      for (const [k, v] of parsed.modifiedCells) {
        // validate v shape
        if (v && typeof v === "object" && ("tokenValue" in v)) {
          modifiedCells.set(k, { tokenValue: v.tokenValue });
        }
      }
    }

    // restore player
    if (typeof parsed.playerLat === "number") playerLat = parsed.playerLat;
    if (typeof parsed.playerLng === "number") playerLng = parsed.playerLng;

    // restore heldToken
    if (parsed.heldToken && typeof parsed.heldToken === "object") {
      // basic validation
      const ht = parsed.heldToken;
      if (
        typeof ht.value === "number" && typeof ht.i === "number" &&
        typeof ht.j === "number"
      ) {
        heldToken = { value: ht.value, i: ht.i, j: ht.j };
      } else {
        heldToken = null;
      }
    } else {
      heldToken = null;
    }

    // hasWon
    hasWon = !!parsed.hasWon;

    // update UI
    updateInventoryUI();
    if (hasWon) {
      winOverlay.style.display = "flex";
    } else {
      winOverlay.style.display = "none";
    }

    return true;
  } catch (err) {
    console.error("Failed to load game:", err);
    return false;
  }
}

