// ------------------- Reset / Initialization -------------------
function resetGame(clearPersistent = true) {
  hasWon = false;
  winOverlay.style.display = "none";
  heldToken = null;
  updateInventoryUI();

  if (clearPersistent) {
    // clear persisted world state
    modifiedCells.clear();
    localStorage.removeItem(STORAGE_KEY);
  }

  // clear transient visuals
  for (const layer of Array.from(cellLayers.values())) {
    map.removeLayer(layer);
  }
  cellLayers.clear();

  for (const m of Array.from(valueMarkers.values())) {
    map.removeLayer(m);
  }
  valueMarkers.clear();

  playerLat = START_LAT;
  playerLng = START_LNG;
  playerMarker.addTo(map);
  updatePlayerMarker();
}

// Initial render/load when map is ready
map.whenReady(() => {
  const found = loadGame();
  if (!found) {
    resetGame(true);
  } else {
    // restored from storage: ensure visuals reflect restored state
    playerMarker.addTo(map);
    updatePlayerMarker();
    renderGrid();
  }
});

map.on("moveend", renderGrid);
